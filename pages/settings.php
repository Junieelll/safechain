    function handleLogoUpload(input) {
        const file = input.files[0];
        if (!file) return;
        if (file.size > 2 * 1024 * 1024) { showToast('Logo must be under 2MB', 'error'); return; }
        const reader = new FileReader();
        reader.onload = e => {
            const img = document.getElementById('logoPreview');
