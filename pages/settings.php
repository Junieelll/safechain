    function testNotificationSound() {
        const volume   = parseInt(document.getElementById('notification_volume').value, 10) / 100;
        const duration = Math.max(1, Math.min(30, parseInt(document.getElementById('notification_duration').value, 10) || 3)) * 1000;

        // Resolve the audio source
        let src = null;
        if (pendingSoundFile) {
            src = URL.createObjectURL(pendingSoundFile);
        } else {
            // Use saved path or fallback default
            const savedPath = '<?= !empty($settings['notification_sound']) ? addslashes(s($settings, 'notification_sound')) : '' ?>';
            src = savedPath ? savedPath : '/assets/sounds/alert.mp3';
        }

        if (testTimer) { clearTimeout(testTimer); testTimer = null; }
        const audio = new Audio(src);
        audio.volume = volume;
        audio.play().catch(() => showToast('Browser blocked autoplay — interact with the page first.', 'error'));

        // Show waveform
        const wv = document.getElementById('soundWaveform');
        const st = document.getElementById('soundStatus');
        const tb = document.getElementById('testSoundBtn');
        wv.classList.remove('hidden');
        wv.classList.add('flex');
        st.classList.remove('hidden');
        tb.disabled = true;

        testTimer = setTimeout(() => {
            audio.pause();
            audio.currentTime = 0;
            wv.classList.add('hidden');
            wv.classList.remove('flex');
            st.classList.add('hidden');
            tb.disabled = false;
        }, duration);
    }

