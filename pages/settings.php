    function handleSoundUpload(input) {
        const file = input.files[0];
        if (!file) return;
        const maxMB = 5;
        if (file.size > maxMB * 1024 * 1024) {
            showToast(`Audio file must be under ${maxMB}MB`, 'error');
            input.value = '';
            return;
        }
        pendingSoundFile = file;
        document.getElementById('soundFileName').textContent = file.name;
        document.getElementById('notification_sound_cleared').value = '0';

        // Build a local object URL for immediate test playback
        if (previewAudio) { previewAudio.pause(); previewAudio = null; }
        previewAudio = new Audio(URL.createObjectURL(file));

        document.body.classList.add('dirty');
        showToast('Sound file ready — click "Test Sound" to preview or save to apply.', 'success');
    }

